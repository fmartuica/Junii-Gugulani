<div style="text-indent: 40pt;" >După o lungă pauză, care a ținut pe perioada postului Paștelui, Ansamblul Junii Gugulani și-a reluat activitatea. Prima sa activitate după această pauză a fost Ruga de la Caransebeș, care s-a desfășurat din data de 12 aprilie, până în data de 15 aprilie.</div>

   <div style="text-indent: 40pt;"> Ansamblul Junii Gugulani și-a desfășurat activitatea pe parcursul a 2 zile. Prima zi de spectacol, în care Ansamblul Junii Gugulani și-a desfășurat activitatea, a fost ce-a de a doua zi de rugă. În această zi, tinerii dansatori au debutat cu noua lor suită din repertoriul ansamblului, o suită de pe Valea Sebeșului cu tema ,,Ruga bănățeană”, coregrafia semnată de Adrian Jurchescu. Această suită o puteți viziona <a href="video2.php">aici.</a></div>

     <div style="text-indent: 40pt;">  Cea de a doua zi în care Ansamblul junii Gugulani și-a desfășurat activitatea, a fost ultima zi de rugă, și anume Duminica Tomii. În această zi, activitățile culturale s-au desfășurat în fața Catedralei Episcopale ,,Învierea Domnului” din Caransebeș. Aici au luat parte mai multe oficialități, dar și diferite Capete Bisericești din toata țara.</div><br><br>
