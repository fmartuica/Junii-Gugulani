Because the Ensemble Junii Gugulani ensemble is an open ensemble for new experiences, he has accepted with great joy the invitation to take part in the National Festival (contest), "I Sing in Banat", I. This contest was organized this year , for the first time by Nicusor Ionita Music.</div>
<div style="text-indent: 40pt;" >
The young dancers put down a suite on the Bistrita Valley. This was originally a suite of girdles and twos, but for this contest, the young choreographer, Adrian Jurchescu, full of ideas, as he is, decided to add to the composition of the suite and a Transylvanian, to raise the level of difficulty. This decision was taken only 3 days before the contest and only one rehearsal was available to apply this amendment.
<div style="text-indent: 40pt;" > 
During the contest, both the dancers and the choreographer Adrian Jurchescu, as well as the coordinator Adela Jurchescu (sister of the choreographer), were full of emotions due to the change in the suite. To the joy of Junii Gugulani, they returned home with the Popular Folk Dance Trophy.</div><br><br>
