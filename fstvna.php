<div style="text-indent: 40pt;" >  Deoarece Ansamblul Junii Gugulani este un ansamblu deschis spre noi experiențe, el a acceptat cu mare bucurie invitația de a lua parte la Festivalul Național (concurs) ,,Cântu’ meu îi din Banat”-ediția I. Acest concurs a fost organizat în acest an, pentru prima dată de Nicușor Ioniță Music.</div>
<div style="text-indent: 40pt;" >Tinerii dansatori au pus în șcenă o suită de pe Valea Bistrei. Aceasta a fost inițial o suită de brâuri și doiuri, însă pentru acest concurs, tânărul coregraf, Adrian Jurchescu, plin de idei, așa cum este dumnealui, de fel, a hotărât să mai adauge în componența suitei și o ardeleană, pentru ridicarea nivelului de dificultate. Această hotărâre a luat-o, cu numai cu 3 zile înainte de concurs și a avut la dispoziție o singură repetiție pentru aplicarea acestei modificări.
<div style="text-indent: 40pt;" > În timpul concursului, atât dansatorii, cât și coregraful Adrian Jurchescu, dar și coordonatoarea Adela Jurchescu (sora domnului coregraf), au fost plini de emoții, datorită schimbării aduse asupra suitei. Spre bucuria Junilor Gugulani, aceștia s-au întors acasă cu Trofeul secțiunii de dans popular.</div><br><br>
